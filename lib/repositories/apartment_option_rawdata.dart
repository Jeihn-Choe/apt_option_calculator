  // 특정 조건의 데이터 찾기
  static Map<String, dynamic>? getApartmentData({
    required String type,
    required bool bedSep,
    bool? alphaSep,
  }) {
    try {
      return data.firstWhere(
        (item) =>
            item['type'] == type &&
            item['bedSep'] == bedSep &&
            item['alphaSep'] == alphaSep,
      );
    } catch (e) {
      return null;
    }
  }

  // 사용 가능한 아파트 타입들 가져오기
  static List<String> getAvailableTypes() {
    return data.map((item) => item['type'] as String).toSet().toList()..sort();
  }
